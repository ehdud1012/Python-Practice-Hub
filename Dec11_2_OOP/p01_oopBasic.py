# Java
#   Perfect한 OOL : 무조건 OOP로
#   규칙의 언어 -> 정돈
#   project
#       Java 용어 겸 일상 용어 project
#       project 개념이 중요
#   package
#       class명 중복을 피하고, 구별할 수 있는 수단으로 사용
#       ===> 필수
#       만약 패키지명까지 중복되면 → 망한거임 (중복 안되게 잘 짓기)
#           => 그래서 패키지명 짓는 문화 (약속, 규칙) - 회사명.프로그램명.카테고리
#   class
#       JavaBean이라 부를정도로 필수
#       1 class = 1 file (재사용, 주고받기 편하려고)
#       class명 첫글자 대문자로 짓는 문화(규칙)
# 
# 
#   
# Python
#   hybrid한 OOL : OOP를 하든 말든
#   자유의 언어 -> 혼란
#   project
#       일상용어 project
#       project 개념 없음 -> 그냥 폴더 만들어서 하던대로 정리한 것뿐  
#   package
#       Java랑 동일하게 개발자 많고, 공유함
#       class명 중복을 피하고, 구별할 수 있는 수단으로 사용
#           근데? ==> 필수 아님 (중복되면 되는거지 머..)
#       패키지명까지 중복되면 → 망한거임
#           근데? ==> 짓는 규칙 없음
#   module
#       Python 소스 파일 (.py)
#       1 module = 1 file
#  
#   class
#       OOP 필수 X -> 필수 X
#       1 class != 1 file
#        class명 첫글자 대문자로 하든 말든